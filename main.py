#Processor Status
flags = 'CZIDB1VN'

def SEF(flag):  		#Set Flag
	global flags, P
	bits = flags.index(flag)
	P = P | (1<<bits) 	#move one to appropriate bit and if 0 make 1 (0|1=1, 1|1=1)

def CLF(flag):  		#Clear Flag
	global flags, P
	bits = flags.index(flag)
	mask = 0b11111111-(1<<bits) #i.e. bits = 4, 1<<bits = 00010000, 11111111-00010000=11101111
	P = P & mask 		#the one bit thats 0 gets anded with the bit we're checking for
	 					#all other bits = 1 so if checking byte bit is 0 we get 0 and is 1 we get 1 hooray!

