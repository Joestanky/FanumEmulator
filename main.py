#Basic Byte Manipulation
def ByteCheck(byte):
	if byte >=256: 	#overflow
		byte -= 256
	elif byte <0:
		byte += 256 #underflow
	return byte

def WordCheck(word):
	if word >=0x10000: 	#overflow
		word -= 0x10000
	elif word <0:  		#underflow
		word += 0x10000
	return word

def Add(b):
	global ALU
	return ByteCheck(ALU+b)
#--
def IncPC():
	global PC
	PC = WordCheck(PC+1)

def DecPC():
	global PC
	PC = WordCheck(PC-1)

def PAB(): #Pc to AddressBus
	global AB, PC
	AB = PC
	return(AB)

def AHL(): #ADH and ADL combined to AB
	global AB
	AB = (ADH<<8)+ADL
	return AB

