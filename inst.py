def LDAZP(): #load to a from zero page address, 3 Cycs, $A5
	if glob.Cyc == 2:
		glob.ADL = glob.DB 	#Get zero page address
		glob.ADH = 0x00 	#set high to zero page 
		AHL() 		#Combine ADH and ADL to 16-bit AB

